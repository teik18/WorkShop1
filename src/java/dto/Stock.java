package dto;

/**
 *
 * @author admin
 */
public class Stock {
    private String ticker;
    private String name;
    private String sector;
    private float price;
    private boolean status;

    public Stock() {
    }

    public Stock(String ticker, String name, String sector, float price, boolean status) {
        this.ticker = ticker;
        this.name = name;
        this.sector = sector;
        this.price = price;
        this.status = status;
    }

    public String getTicker() {
        return ticker;
    }

    public void setTicker(String ticker) {
        this.ticker = ticker;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSector() {
        return sector;
    }

    public void setSector(String sector) {
        this.sector = sector;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }

    public boolean isStatus() {
        return status;
    }

    public void setStatus(boolean status) {
        this.status = status;
    }
    
}
